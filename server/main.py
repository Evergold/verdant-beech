_tool_cache = None
_tool_cache_mtime = 0

def get_tool_registry():
    global _tool_cache, _tool_cache_mtime
    tools_path = os.path.join(os.path.dirname(__file__), "tools.yaml")
    if not os.path.exists(tools_path):
        return []
        
    current_mtime = os.path.getmtime(tools_path)
    if _tool_cache is not None and current_mtime == _tool_cache_mtime:
        return _tool_cache
        
    with open(tools_path, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f)
        _tool_cache = data.get("tools", [])
        _tool_cache_mtime = current_mtime
        
    return _tool_cache

@app.post("/api/chat")
async def chat_endpoint(req: ChatRequest, background_tasks: BackgroundTasks):
    total_msgs = len(req.messages)
    compacted_idx = 0
    revery_profile = []
    
    if req.project_id:
        data = load_projects()
        if req.project_id in data["projects"]:
            compacted_idx = data["projects"][req.project_id].get("compacted_idx", 0)
            revery_profile = data["projects"][req.project_id].get("revery_profile", [])
            
            if total_msgs - compacted_idx > 20:
                to_compact = req.messages[compacted_idx : total_msgs - 10]
                new_compacted_idx = total_msgs - 10
                
                data["projects"][req.project_id]["compacted_idx"] = new_compacted_idx
                save_projects(data)
                
                # Offload background tasks to the lightweight 2.3B model
                background_tasks.add_task(compact_memory, req.project_id, to_compact, "ollama_chat/gemma4:e2b", compacted_idx, new_compacted_idx)

    # PRE-INJECTION RAG & ELASTIC WINDOW
    latest_user_msg = req.messages[-1].content if req.messages else ""
    rag_context = ""
    revery_context = ""
    episodic_messages = []
    
    if latest_user_msg:
        # 1. Cartography Rules
        rag_results = rag_store.query(latest_user_msg, n_results=2)
        if rag_results:
            rag_context = "\n\nEXPERT CARTOGRAPHY RULES TO FOLLOW:\n- " + "\n- ".join(rag_results)
            
        # 2. Revery Profile (Semantic Memory)
        if revery_profile:
            revery_context = "\n\nABSOLUTE USER FACTS (NEVER FORGET):\n- " + "\n- ".join(revery_profile)
            
        # 3. Elastic Window (Time-Aware Episodic Recall)
        if req.project_id:
            try:
                mem_collection = rag_store.client.get_or_create_collection(
                    name=f"memory_{req.project_id}",
                    embedding_function=rag_store.embedding_function
                )
                mem_results = mem_collection.query(query_texts=[latest_user_msg], n_results=5)
                if mem_results and mem_results.get('metadatas') and mem_results['metadatas'][0]:
                    slices = mem_results['metadatas'][0]
                    slices.sort(key=lambda x: x.get("timestamp", 0))
                    
                    for s in slices:
                        start = s.get("start_idx")
                        end = s.get("end_idx")
                        if start is not None and end is not None and start < compacted_idx:
                            actual_end = min(end, compacted_idx)
                            episodic_messages.extend(req.messages[start:actual_end])
            except Exception as e:
                print(f"[MEMORY] Error fetching episodic memory: {e}")

    # DYNAMIC TOOL GATE: Only load tools if relevant keywords are in the user's latest message
    active_tools = []
    active_few_shot = []
    latest_lower = latest_user_msg.lower()
    
    for tool in get_tool_registry():
        if any(kw in latest_lower for kw in tool["keywords"]):
            active_tools.append(tool["schema"])
            active_few_shot.append(tool["few_shot"])
            
    # Build dynamic prompt
    system_prompt = CARTOGRAPHER_PROMPT
    if active_few_shot:
        system_prompt += "\n\nTOOL CALLING EXAMPLES:\n" + "\n".join(active_few_shot)

    # Build the final prompt structure (Bottom-Heavy Architecture)
    messages = [{"role": "system", "content": system_prompt}]
    
    if episodic_messages:
        messages.append({"role": "system", "content": "--- TELEPORTED HISTORICAL CONTEXT ---"})
        messages.extend([{"role": m.role, "content": m.content} for m in episodic_messages])
        messages.append({"role": "system", "content": "--- END HISTORICAL CONTEXT ---"})
    
    recent_working = req.messages[-10:] if total_msgs > 10 else req.messages
    if len(recent_working) > 1:
        messages.extend([{"role": m.role, "content": m.content} for m in recent_working[:-1]])
        
    if rag_context or revery_context:
        bottom_context = "CRITICAL REFRESHER BEFORE RESPONDING:" + revery_context + rag_context
        messages.append({"role": "system", "content": bottom_context})
        
    if recent_working:
        last_m = recent_working[-1]
        messages.append({"role": last_m.role, "content": last_m.content})
    
    kwargs = {
        "model": req.model_name,
        "messages": messages,
        "num_ctx": 2048
    }
    
    # Only attach the JSON schema overhead if the NLP keyword gate passed
    if active_tools:
        kwargs["tools"] = active_tools
    
    if req.reasoning:
        kwargs["reasoning_effort"] = req.reasoning
        
    try:
        import litellm
        response = litellm.completion(**kwargs)
        
        prompt_tokens = getattr(response.usage, 'prompt_tokens', 0) if hasattr(response, 'usage') and response.usage else 0
        if isinstance(prompt_tokens, int) and prompt_tokens > 1800:
            print(f"[MEMORY WARNING] Live chat prompt tokens ({prompt_tokens}) approaching context limit (2048). Truncation risk!")
            
        msg = response.choices[0].message
        
        tool_calls = []
        if hasattr(msg, "tool_calls") and msg.tool_calls:
            for tc in msg.tool_calls:
                tool_calls.append({
                    "id": tc.id,
                    "name": tc.function.name,
                    "arguments": tc.function.arguments
                })
                
        reply_content = msg.content if msg.content else "I have updated the canvas as requested."
        
        return {
            "reply": reply_content,
            "tool_calls": tool_calls
        }
    except Exception as e:
        return {"reply": f"Error: {str(e)}"}

