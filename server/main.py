async def auto_setup_models():
    setup_status["in_progress"] = True
    setup_status["updated"] = False
    models_to_pull = ["ollama_chat/gemma4:e4b".split("/")[-1], "embeddinggemma"]
    
    try:
        async with httpx.AsyncClient() as client:
            for model in models_to_pull:
                async with client.stream("POST", f"{OLLAMA_URL}/api/pull", json={"model": model, "stream": True}, timeout=None) as response:
                    async for line in response.aiter_lines():
                        if line:
                            try:
                                data = json.loads(line)
                                if data.get("status") == "downloading" and data.get("completed", 0) > 0:
                                    setup_status["updated"] = True
                            except:
                                pass
    except Exception as e:
        print(f"[SETUP] Error checking updates: {e}")
    finally:
        setup_status["in_progress"] = False

