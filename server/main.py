import time

async def compact_memory(project_id: str, old_messages: list, model_name: str, start_idx: int, end_idx: int):
    if not old_messages:
        return
        
    if project_id not in subconscious_state:
        subconscious_state[project_id] = {"gathering_thoughts": False, "lost_in_revery": False}
    subconscious_state[project_id]["gathering_thoughts"] = True
    
    try:
        from litellm import acompletion
        prompt = "CRITICAL INSTRUCTION: Act as a highly deterministic, non-creative summarization engine. Do NOT output any <think> tags, reasoning blocks, or internal monologue. Output ONLY the literal, factual final summary without embellishment.\n\n"
        prompt += "EXAMPLE INPUT:\nUSER: Let's make the mountains spiky.\nASSISTANT: Done.\nEXAMPLE OUTPUT: The user decided to make the mountains spiky.\n\n"
        prompt += "Summarize the following conversation segment into a single, concise sentence focusing on decisions made, user preferences, and cartography progress:\n\n"
        for msg in old_messages:
            role = msg["role"] if isinstance(msg, dict) else msg.role
            content = msg["content"] if isinstance(msg, dict) else msg.content
            prompt += f"{str(role).upper()}: {str(content)}\n"
        
        res = await acompletion(
            model=model_name,
            messages=[{"role": "user", "content": prompt}],
            max_tokens=150,
            reasoning_effort="low",
            num_ctx=1024,
            drop_params=True
        )
        summary = res.choices[0].message.content.strip()
        
        prompt_tokens = getattr(res.usage, 'prompt_tokens', 0) if hasattr(res, 'usage') and res.usage else 0
        if isinstance(prompt_tokens, int) and prompt_tokens > 900:
            print(f"[MEMORY WARNING] Summarizer prompt tokens ({prompt_tokens}) approaching context limit (1024). Truncation risk!")
        
        from server.rag import rag_store
        import uuid
        mem_collection = rag_store.client.get_or_create_collection(
            name=f"memory_{project_id}",
            embedding_function=rag_store.embedding_function
        )
        mem_collection.upsert(
            documents=[summary],
            metadatas=[{"type": "episodic_summary", "start_idx": start_idx, "end_idx": end_idx, "timestamp": time.time()}],
            ids=[str(uuid.uuid4())]
        )
        print(f"[MEMORY MANAGER] Compacted {len(old_messages)} messages into ChromaDB for project {project_id}.")
        
        # REVERY: Semantic Profile Engine (with NLP Gating)
        subconscious_state[project_id]["gathering_thoughts"] = False
        directives = ["want", "prefer", "change", "remove", "always", "never", "add", "must", "should", "hate", "love"]
        if any(d in summary.lower() for d in directives):
            subconscious_state[project_id]["lost_in_revery"] = True
            data = load_projects()
            profile = data["projects"][project_id].get("revery_profile", [])
            
            revery_prompt = (
                f"Analyze this conversation summary:\n'{summary}'\n\n"
                f"Current permanent facts about the user's project:\n{profile}\n\n"
                "If the summary reveals a NEW permanent fact (e.g. style preference, biome, rule), output a new line starting exactly with 'ADD: <fact>'.\n"
                "If the summary contradicts an existing fact, output 'REMOVE: <old fact>' or 'UPDATE: <old fact> -> <new fact>'.\n"
                "If there is nothing new or actionable, output nothing. Do not use markdown or quotes.\n\n"
                "EXAMPLES:\n"
                "- ADD: The user prefers a dark fantasy aesthetic.\n"
                "- REMOVE: The map is a snowy tundra.\n"
                "- UPDATE: The central region is a valley -> The central region is a massive crater."
            )
            
            rev_res = await acompletion(
                model=model_name,
                messages=[{"role": "user", "content": revery_prompt}],
                max_tokens=150,
                reasoning_effort="low",
                num_ctx=1024,
                drop_params=True
            )
            rev_out = rev_res.choices[0].message.content.strip()
            
            changed = False
            for line in rev_out.splitlines():
                line = line.strip()
                if line.startswith("ADD:"):
                    new_fact = line.replace("ADD:", "").strip()
                    # Semantic Gate Deduplication
                    if profile:
                        new_emb = rag_store.embedding_function([new_fact])[0]
                        prof_embs = rag_store.embedding_function(profile)
                        def cos_sim(a, b):
                            return sum(x*y for x, y in zip(a,b)) / ((sum(x*x for x in a)**0.5 * sum(x*x for x in b)**0.5) or 1)
                        if any(cos_sim(new_emb, p_emb) > 0.85 for p_emb in prof_embs):
                            continue # Duplicate fact
                    profile.append(new_fact)
                    changed = True
                    
                elif line.startswith("REMOVE:"):
                    target = line.replace("REMOVE:", "").strip()
                    if profile:
                        tgt_emb = rag_store.embedding_function([target])[0]
                        prof_embs = rag_store.embedding_function(profile)
                        def cos_sim(a, b):
                            return sum(x*y for x, y in zip(a,b)) / ((sum(x*x for x in a)**0.5 * sum(x*x for x in b)**0.5) or 1)
                        best_idx = max(range(len(profile)), key=lambda i: cos_sim(tgt_emb, prof_embs[i]))
                        if cos_sim(tgt_emb, prof_embs[best_idx]) > 0.70:
                            profile.pop(best_idx)
                            changed = True
                            
                elif line.startswith("UPDATE:"):
                    parts = line.replace("UPDATE:", "").split("->")
                    if len(parts) == 2:
                        target = parts[0].strip()
                        new_fact = parts[1].strip()
                        if profile:
                            tgt_emb = rag_store.embedding_function([target])[0]
                            prof_embs = rag_store.embedding_function(profile)
                            def cos_sim(a, b):
                                return sum(x*y for x, y in zip(a,b)) / ((sum(x*x for x in a)**0.5 * sum(x*x for x in b)**0.5) or 1)
                            best_idx = max(range(len(profile)), key=lambda i: cos_sim(tgt_emb, prof_embs[i]))
                            if cos_sim(tgt_emb, prof_embs[best_idx]) > 0.70:
                                profile[best_idx] = new_fact
                                changed = True
            
            if changed:
                data["projects"][project_id]["revery_profile"] = profile
                save_projects(data)
                print(f"[REVERY] Updated semantic profile for {project_id}")

    except Exception as e:
        print(f"[MEMORY MANAGER] Error compacting memory: {e}")
    finally:
        if project_id in subconscious_state:
            subconscious_state[project_id]["gathering_thoughts"] = False
            subconscious_state[project_id]["lost_in_revery"] = False

